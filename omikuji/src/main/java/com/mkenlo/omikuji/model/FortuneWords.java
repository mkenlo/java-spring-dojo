package com.mkenlo.omikuji.model;

public class FortuneWords {
    private int year;
    private String city;
    private String person;
    private String hobby;
    private String livingThing;
    private String niceMessage;

    public FortuneWords() {

    }

    /**
     * @return int return the year
     */
    public int getYear() {
        return year;
    }

    /**
     * @param year the year to set
     */
    public void setYear(int year) {
        this.year = year;
    }

    /**
     * @return String return the city
     */
    public String getCity() {
        return city;
    }

    /**
     * @param city the city to set
     */
    public void setCity(String city) {
        this.city = city;
    }

    /**
     * @return String return the person
     */
    public String getPerson() {
        return person;
    }

    /**
     * @param person the person to set
     */
    public void setPerson(String person) {
        this.person = person;
    }

    /**
     * @return String return the hobby
     */
    public String getHobby() {
        return hobby;
    }

    /**
     * @param hobby the hobby to set
     */
    public void setHobby(String hobby) {
        this.hobby = hobby;
    }

    /**
     * @return String return the livingThing
     */
    public String getLivingThing() {
        return livingThing;
    }

    /**
     * @param livingThing the livingThing to set
     */
    public void setLivingThing(String livingThing) {
        this.livingThing = livingThing;
    }

    /**
     * @return String return the niceMessage
     */
    public String getNiceMessage() {
        return niceMessage;
    }

    /**
     * @param niceMessage the niceMessage to set
     */
    public void setNiceMessage(String niceMessage) {
        this.niceMessage = niceMessage;
    }

}
